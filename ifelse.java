import java.util.*;

public class ifelse {
    
    public static void main(String[] args) {
        
        //inputs
        Scanner sc = new Scanner(System.in);
        int  n = sc.nextInt();
        if (n % 2 == 0) {
            System.out.println("even");
        }
         else {
            System.out.println("odd");
        }
    }

}
